type CVAProps = {
  className?: string;
  variant?: "purple" | "original" | "default" | "destructive" | "outline" | "secondary" | "ghost" | "link" | null | undefined;
  size?: "default" | "sm" | "lg" | "icon" | null | undefined;
};

const MenubarVariants = cva("flex h-10 items-center rounded-md border", {
  variants: {
    variant: {
      purple: "bg-purple hover:bg-darkPurple text-primary-light",
      original: "space-x-1 p-1",
      default: "",
      destructive: "bg-destructive text-destructive-foreground hover:bg-destructive/90",
      outline: "border border-input bg-background hover:bg-accent hover:text-accent-foreground",
      secondary: "bg-secondary text-secondary-foreground hover:bg-secondary/80",
      ghost: "hover:bg-accent hover:text-accent-foreground",
      link: "text-primary underline-offset-4 hover:underline",
    },
    width: {
      full: "w-full justify-center",
      fit: "w-fit",
    },
    size: {
      default: "",
      sm: "h-9 rounded-md px-3",
      lg: "h-11 rounded-md px-8",
      icon: "h-10 w-10",
    },
  },
  defaultVariants: {
    variant: "default",
    size: "default",
  },
});

const Menubar = React.forwardRef<
  React.ComponentRef<typeof MenubarPrimitive.Root>,
  CVAProps & React.ComponentPropsWithoutRef<typeof MenubarPrimitive.Root>
>(({ className, variant, size, ...props }, ref) => (
  <MenubarPrimitive.Root ref={ref} className={cn(MenubarVariants({ variant, size, className }))} {...props} />
));
Menubar.displayName = MenubarPrimitive.Root.displayName;

